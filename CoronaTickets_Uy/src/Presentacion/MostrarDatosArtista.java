    public MostrarDatosArtista(String prefix, Artista artista) {
        initComponents();
        setTitle(prefix + " - " + getTitle());
        this.artista = artista;
        nickname.setText(artista.getNickname());
        nombre.setText(artista.getNombre());
        apellido.setText(artista.getApellido());
        correo.setText(artista.getCorreo());

        String pattern = "yyyy-MM-dd";
        SimpleDateFormat format = new SimpleDateFormat(pattern);
        fecha_nacimiento.setText(format.format(artista.getNacimiento()));
        
        descripcion.setText(artista.getDescripcion());
        biografia.setText(artista.getBiografia());
        sitio_web.setText(artista.getSitio_web());
        
        espectaculos = Fabrica.getInstance().getInstanceControllerUsuario().obtener_espectaculos_organizados_por_artista(artista.getId());
        ArrayList<String> lista = new ArrayList<>();
        espectaculos.forEach((espectaculo) -> {
            lista.add(espectaculo.getNombre());
        });

        lista_espectaculos.setListData((String[]) lista.toArray(new String[lista.size()]));

        panel_de_espectaculo_no_seleccion.setVisible(true);
        panel_de_espectaculo_datos.setVisible(false);
    }

