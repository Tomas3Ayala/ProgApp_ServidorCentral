    private void lista_espectaculosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lista_espectaculosValueChanged
        String item = (String) lista_espectaculos.getSelectedValue();
        if (item != prev_esspectaculos_item && item != null) {
            System.out.println(item);
            prev_esspectaculos_item = item;
            
            panel_de_espectaculo_no_seleccion.setVisible(false);
            panel_de_espectaculo_datos.setVisible(true);
            
            Espectaculo espectaculo = espectaculos.get(lista_espectaculos.getSelectedIndex());
            label_plataforma_espectaculo.setText(espectaculo.getPlataforma());
            label_nombre_espectaculo.setText(espectaculo.getNombre());
            label_descripcion_espectaculo.setText(espectaculo.getDescripcion());
            label_duracion_espectaculo.setText(Integer.toString(espectaculo.getDuracion()));
            label_min_espectaculo.setText(Integer.toString(espectaculo.getMin_espectador()));
            label_max_espectaculo.setText(Integer.toString(espectaculo.getMax_espectador()));
            label_url_espectaculo.setText(espectaculo.getUrl());
            label_costo_espectaculo.setText("$" + Integer.toString(espectaculo.getCosto()));
            label_fecha_registro_espectaculo.setText(espectaculo.getFecha_registro().toString());
        }
    }//GEN-LAST:event_lista_espectaculosValueChanged

