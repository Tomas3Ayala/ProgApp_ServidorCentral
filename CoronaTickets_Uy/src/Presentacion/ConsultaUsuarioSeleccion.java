    private void button_ver_perfilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_ver_perfilActionPerformed
        if (!lista_usuarios.isSelectionEmpty()) {
            if(Fabrica.getInstance().getInstanceControllerUsuario().chequear_si_nickname_es_de_artista(lista_usuarios.getSelectedValue())) {
                if (ventana_mostrar_datos_espectador != null && ventana_mostrar_datos_espectador.isDisplayable())
                    ventana_mostrar_datos_espectador.dispose();
                if (ventana_mostrar_datos_artista != null && ventana_mostrar_datos_artista.isDisplayable())
                    ventana_mostrar_datos_artista.dispose();
                
                ventana_mostrar_datos_artista = new MostrarDatosArtista(this.getTitle(), Fabrica.getInstance().getInstanceControllerUsuario().obtener_artista_de_nickname(lista_usuarios.getSelectedValue()));
                ventana_mostrar_datos_artista.setVisible(true);
//                System.out.println("es artista!");
            }
            else {
                if (ventana_mostrar_datos_espectador != null && ventana_mostrar_datos_espectador.isDisplayable())
                    ventana_mostrar_datos_espectador.dispose();
                if (ventana_mostrar_datos_artista != null && ventana_mostrar_datos_artista.isDisplayable())
                    ventana_mostrar_datos_artista.dispose();

                ventana_mostrar_datos_espectador = new MostrarDatosEspectador(this.getTitle(), Fabrica.getInstance().getInstanceControllerUsuario().obtener_espectador_de_nickname(lista_usuarios.getSelectedValue()));
                ventana_mostrar_datos_espectador.setVisible(true);
            }
        }
        else
            JOptionPane.showMessageDialog(this, "Primero seleccione un usuario para ver su perfil");
    }//GEN-LAST:event_button_ver_perfilActionPerformed

